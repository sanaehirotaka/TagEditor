    public int TotalWeight { get; init; }

    public BuildinTags()
    {
        TotalWeight = Tags.Sum(item => item.Score);
    }

    public IEnumerable<Tag> RandomSelectTag(int count)
    {
        var tags = new HashSet<Tag>();

        while (tags.Count < count)
        {
            int cumulativeWeight = 0;
            int randomNumber = Random.Shared.Next(TotalWeight);
            foreach (var tag in Tags)
            {
                cumulativeWeight += tag.Score;
                if (randomNumber < cumulativeWeight)
                {
                    tags.Add(tag);
                    break;
                }
            }
        }
        return tags.OrderByDescending(tag => tag.Score);
    }

